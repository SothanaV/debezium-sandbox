-- Create a sample application user and table to monitor
CREATE USER c##appuser IDENTIFIED BY apppassword CONTAINER=ALL;
GRANT CONNECT, RESOURCE TO c##appuser CONTAINER=ALL;
ALTER USER c##appuser QUOTA UNLIMITED ON USERS;

CREATE TABLE c##appuser.customers (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(50),
    email VARCHAR2(50)
);

-- Enable supplemental logging specifically for this table
ALTER TABLE c##appuser.customers ADD SUPPLEMENTAL LOG DATA (ALL) COLUMNS;